#define GET_HIR()		(airoha_get_pid() >> 16)
#define GET_PACKAGE_ID()	(airoha_get_pid() & 0x1f)
#define GET_PDIDR()		(airoha_get_pdidr() & 0xffff)

#define EN7523_HIR		(0x0c)
#define EN7581_HIR		(0x0e)
#define AN7552_HIR		(0x0f)
#define AN7583_HIR		(0x10)

#define isEN7523		(GET_HIR() == EN7523_HIR)
#define isEN7581		(GET_HIR() == EN7581_HIR)
#define isAN7552		(GET_HIR() == AN7552_HIR)
#define isAN7583		(GET_HIR() == AN7583_HIR)

